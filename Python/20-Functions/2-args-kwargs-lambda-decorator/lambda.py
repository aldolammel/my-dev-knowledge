"""
FUNCTIONS (PART 3): LAMBDA FUNCTIONS

Lambda functions take input and return outputs.
It is a simpler way to write a function but not truly helpful.

Syntax: lambda <list of parameters>: <return value of the function>
Example: _divider = lambda x, y: x / y

"""

_divider = lambda x, y: x / y                         # example of lambda function.

print('Result: ', _divider(15, 3))
