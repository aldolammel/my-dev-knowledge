

// ARRAY: ADDING ELEMENTS
// .push() method in JavaScript is used to add one or more elements to the end of an array.


// Initialize an array
let fruits = ['apple', 'banana'];

// Use .push() to add a new element
fruits.push('orange', 'watermellow');

console.log(fruits); // Output: ['apple', 'banana', 'orange', 'watermellow']